#include "libft/bytes.h"

uint32_t	swap_int32(const uint32_t data)
{
	return (((data & 0xff000000) >> 24) |
			((data & 0x00ff0000) >> 8) |
			((data & 0x0000ff00) << 8) |
			((data & 0x000000ff) << 24));
}